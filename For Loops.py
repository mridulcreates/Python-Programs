# Print Letters Of A Word
for i in "banana":
  print(i)

# Print Numbers 1-5
for i in range(1, 6):
  print(i)

# Print the table of 3
for x in range(3, 30, 3):
  print(x)

# Print * ** Pattern
for i in range(1, 5):
  print('*' * i)

# Print n odd numbers
for i in range(10):
  odd = 2 * i + 1
  print(odd)